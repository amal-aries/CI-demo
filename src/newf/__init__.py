"""Utility functions for simple math operations."""

def add(a: int, b: int) -> int:
    """
    Return the sum of two integers.

    Parameters
    ----------
    a : int
        First addend.
    b : int
        Second addend.

    Returns
    -------
    int
        The sum of ``a`` and ``b``.
    """
    return a + b


def multiply(a: int, b: int) -> int:
    """
    Return the product of two integers.

    Parameters
    ----------
    a : int
        First factor.
    b : int
        Second factor.

    Returns
    -------
    int
        The product of ``a`` and ``b``.
    """
    return a * b
